// Get verified sources for a specific dimension
export function getVerifiedSources(dimension) {
  const dimensionMap = {
    'Market Alignment': 'market_alignment',
    'Budget Readiness': 'budget_readiness',
    'Technology Fit': 'technology_fit',
    'Competitive Position': 'competitive_position',
    'Implementation Readiness': 'implementation_readiness'
  }
  
  const key = dimensionMap[dimension] || 'fallback'
  const sources = VERIFIED_SOURCES[key] || VERIFIED_SOURCES.fallback
  
  // Return sources with quotes
  return sources.map(source => ({
    title: source.title,
    url: source.url,
    quote: source.quote,
    relevance: source.relevance
  }))
}

// Validate that a URL is accessible (for production use, this would make actual HTTP requests)
export function validateSourceUrl(url) {
  // Check if URL is in our verified list
  const allSources = Object.values(VERIFIED_SOURCES).flat()
  const isVerified = allSources.some(source => source.url === url)
  
  return {
    valid: isVerified,
    verified: isVerified,
    status: isVerified ? 200 : 404
  }
}

// Get a guaranteed working source for any dimension
export function getGuaranteedSource(dimension) {
  const sources = getVerifiedSources(dimension)
  
  // Return the first verified source (guaranteed to work)
  if (sources.length > 0) {
    return sources[0]
  }
  
  // Fallback to generic source
  return VERIFIED_SOURCES.fallback[0]
}

// Format source with quote for display
export function formatSourceWithQuote(source) {
  return {
    title: source.title,
    url: source.url,
    display: `"${source.quote}"`,
    relevance: source.relevance
  }
}

// Get multiple verified sources with quotes
export function getMultipleVerifiedSources(dimension, count = 2) {
  const sources = getVerifiedSources(dimension)
  const selected = sources.slice(0, count)
  
  // If we don't have enough sources, add fallbacks
  while (selected.length < count) {
    const fallback = VERIFIED_SOURCES.fallback[selected.length % VERIFIED_SOURCES.fallback.length]
    selected.push(fallback)
  }
  
  return selected.map(formatSourceWithQuote)
}