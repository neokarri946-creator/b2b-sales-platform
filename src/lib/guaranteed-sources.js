// Get guaranteed working sources with quotes
export function getGuaranteedSources(dimension) {
  const dimensionMap = {
    'Market Alignment': 'market_alignment',
    'Budget Readiness': 'budget_readiness',
    'Technology Fit': 'technology_fit',
    'Competitive Position': 'competitive_position',
    'Implementation Readiness': 'implementation_readiness'
  }
  
  const key = dimensionMap[dimension] || 'market_alignment'
  const sources = GUARANTEED_SOURCES[key]
  
  // Return 2-3 sources with full quotes
  return sources.slice(0, 3).map(source => ({
    title: source.title,
    url: source.url,
    quote: source.quote,
    relevance: source.relevance,
    display: `"${source.quote}"`
  }))
}

// Get a mix of sources to ensure variety
export function getMixedSources(dimension) {
  const sources = getGuaranteedSources(dimension)
  
  // Ensure we always have at least 2 sources
  if (sources.length < 2) {
    sources.push(GUARANTEED_SOURCES.market_alignment[0])
  }
  
  return sources
}

// Validate URL is from our guaranteed list
export function isGuaranteedUrl(url) {
  const allSources = Object.values(GUARANTEED_SOURCES).flat()
  return allSources.some(source => source.url === url)
}

// Get source by URL
export function getSourceByUrl(url) {
  const allSources = Object.values(GUARANTEED_SOURCES).flat()
  return allSources.find(source => source.url === url)
}