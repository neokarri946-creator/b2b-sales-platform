// Get enterprise-grade sources for a dimension
export function getEnterpriseSources(dimension) {
  const dimensionMap = {
    'Market Alignment': 'market_alignment',
    'Budget Readiness': 'budget_readiness',
    'Technology Fit': 'technology_fit',
    'Competitive Position': 'competitive_position',
    'Implementation Readiness': 'implementation_readiness'
  }
  
  const key = dimensionMap[dimension]
  if (!key) return []
  
  const sources = ENTERPRISE_SOURCES[key]
  
  // Return sources with full details
  return sources.map(source => ({
    title: source.title,
    url: source.url,
    quote: source.quote,
    relevance: source.relevance,
    authority: source.authority,
    date: source.date,
    trust_level: source.trust_level,
    display: `"${source.quote}" - ${source.authority}, ${source.date}`
  }))
}

// Validate source trustworthiness
export function validateSourceTrust(source) {
  // Check if source is from our trusted list
  const allSources = Object.values(ENTERPRISE_SOURCES)
    .filter(Array.isArray)
    .flat()
  
  const trusted = allSources.find(s => s.url === source.url)
  
  if (trusted) {
    return {
      trusted: true,
      trust_level: trusted.trust_level,
      authority: trusted.authority,
      suitable_for_enterprise: trusted.trust_level === 'VERY_HIGH'
    }
  }
  
  return {
    trusted: false,
    trust_level: 'UNKNOWN',
    suitable_for_enterprise: false
  }
}

// Get only the most trusted sources (for billion-dollar companies)
export function getPremiumSources(dimension, count = 3) {
  const sources = getEnterpriseSources(dimension)
  
  // Filter for only VERY_HIGH trust level
  const premiumSources = sources.filter(s => s.trust_level === 'VERY_HIGH')
  
  // Return requested count
  return premiumSources.slice(0, count)
}

// Get a mix of news and research sources
export function getBalancedSources(dimension) {
  const sources = getEnterpriseSources(dimension)
  
  // Ensure mix of news (WSJ, FT, Bloomberg) and research (HBR, MIT)
  const newsSources = sources.filter(s => 
    s.authority.includes('Wall Street Journal') || 
    s.authority.includes('Financial Times') || 
    s.authority.includes('Bloomberg') ||
    s.authority.includes('Reuters') ||
    s.authority.includes('Forbes')
  )
  
  const researchSources = sources.filter(s => 
    s.authority.includes('Harvard') || 
    s.authority.includes('MIT') ||
    s.authority.includes('McKinsey') ||
    s.authority.includes('Gartner')
  )
  
  // Return balanced mix
  const balanced = []
  if (newsSources.length > 0) balanced.push(newsSources[0])
  if (researchSources.length > 0) balanced.push(researchSources[0])
  if (newsSources.length > 1) balanced.push(newsSources[1])
  
  return balanced.length > 0 ? balanced : sources.slice(0, 3)
}

// Format source citation for enterprise reports
export function formatEnterpriseSource(source) {
  return {
    title: source.title,
    url: source.url,
    quote: source.quote,
    citation: `${source.authority} (${source.date}). ${source.title}. Retrieved from ${source.url}`,
    relevance: source.relevance,
    trust_indicator: source.trust_level === 'VERY_HIGH' ? '✓ Enterprise Verified' : '✓ Verified',
    display: `"${source.quote}"\n\n— ${source.authority}, ${source.date}`
  }
}